function DisplayPullDown() {
  var street = document.getElementById("street").value;
  var addres = document.getElementById("extended-addres");
  addres.innerHTML = ""; // 既存のオプションをクリア

  // 最初のオプションを追加
  var defaultOption = document.createElement("option");
  defaultOption.value = "";
  defaultOption.text = "選択してください";
  defaultOption.hidden = true; // hidden 属性を設定
  addres.appendChild(defaultOption);

  if (array.hasOwnProperty(street)) {
    for (let i = 0; i < array[street].length; i++) {
      var op = document.createElement("option");
      var value = array[street][i];
      op.value = value.cd;
      op.text = value.label;
      addres.appendChild(op);
    }
  }
}



